    [Fact]
    public void Control_AriaDescribedByIncludesErrorIdWhenInvalid()
    {
        // When the field is invalid, the error's ID must appear in aria-describedby.
        var cut = Render<FieldRoot>(p => p
            .Add(c => c.Invalid, true)
            .AddChildContent<FieldDescription>(dp => dp.AddChildContent("Hint"))
            .AddChildContent<FieldControl>()
            .AddChildContent<FieldError>(ep => ep.AddChildContent("Required")));

        var errorId = cut.Find("[aria-live]").GetAttribute("id");
        var describedBy = cut.Find("input").GetAttribute("aria-describedby");

        Assert.NotNull(errorId);
        Assert.NotNull(describedBy);
        Assert.Contains(errorId!, describedBy!);
    }

    [Fact]
    public void Control_AriaDescribedByExcludesErrorIdWhenValid()
    {
        // The error ID must not appear in aria-describedby when the field is valid.
        var cut = Render<FieldRoot>(p => p
            .AddChildContent<FieldDescription>(dp => dp.AddChildContent("Hint"))
            .AddChildContent<FieldControl>()
            .AddChildContent<FieldError>(ep => ep
                .Add(c => c.KeepMounted, true)
                .AddChildContent("Required")));

        var describedBy = cut.Find("input").GetAttribute("aria-describedby");

        if (describedBy is not null)
        {
            // Must not contain the error element's id when valid.
            var errorId = cut.Find("[aria-live]").GetAttribute("id");
            Assert.DoesNotContain(errorId!, describedBy);
        }
    }

