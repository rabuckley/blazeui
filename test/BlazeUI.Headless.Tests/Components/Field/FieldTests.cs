    [Fact]
    public void Item_CascadesItemContext()
    {
        // FieldItem must cascade a FieldItemContext with the correct disabled state
        // so that nested controls can observe it.
        FieldItemContext? capturedContext = null;

        var cut = Render<FieldItem>(p => p
            .Add(c => c.Disabled, true)
            .AddChildContent(b =>
            {
                b.OpenComponent<ContextCapture>(0);
                b.AddComponentParameter(1, nameof(ContextCapture.OnCapture),
                    (Action<FieldItemContext?>)(ctx => capturedContext = ctx));
                b.CloseComponent();
            }));

        Assert.NotNull(capturedContext);
        Assert.True(capturedContext!.Disabled);
    }

    // ─── Cross-component wiring ───────────────────────────────────────────────────

    [Fact]
    public void DisabledStatePropagatesFromRootToAllChildren()
    {
        // The disabled state from FieldRoot flows into all child components via context.
        var cut = Render<FieldRoot>(p => p
            .Add(c => c.Disabled, true)
            .AddChildContent<FieldLabel>(lp => lp.AddChildContent("Label"))
            .AddChildContent<FieldDescription>(dp => dp.AddChildContent("Desc"))
            .AddChildContent<FieldControl>()
            .Add(c => c.Invalid, true)
            .AddChildContent<FieldError>(ep => ep.AddChildContent("Err")));

        // Root itself
        Assert.NotNull(cut.Find("div[data-disabled]"));
        // Label
        Assert.NotNull(cut.Find("label[data-disabled]"));
        // Description
        Assert.NotNull(cut.Find("p[data-disabled]"));
        // Control
        Assert.NotNull(cut.Find("input[data-disabled]"));
        // Error (rendered because Invalid is true)
        Assert.NotNull(cut.Find("[aria-live][data-disabled]"));
    }

    [Fact]
    public void LabelDescriptionErrorAllShareSameControlId()
    {
        // The control, label `for`, and aria-describedby must all reference the same ID.
        var cut = Render<FieldRoot>(p => p
            .Add(c => c.Invalid, true)
            .AddChildContent<FieldLabel>(lp => lp.AddChildContent("Name"))
            .AddChildContent<FieldDescription>(dp => dp.AddChildContent("Hint"))
            .AddChildContent<FieldControl>()
            .AddChildContent<FieldError>(ep => ep.AddChildContent("Err")));

        var controlId = cut.Find("input").GetAttribute("id");
        var labelFor = cut.Find("label").GetAttribute("for");

        Assert.Equal(controlId, labelFor);
    }

    // ─── Consumer ID override ─────────────────────────────────────────────────

    [Fact]
    public void Input_ConsumerIdOverride_PropagatedToLabel()
    {
        // When a consumer provides an explicit id on Input inside a Field,
        // the rendered FieldLabel must emit a matching `for` attribute.
        var cut = Render<FieldRoot>(p => p
            .AddChildContent<FieldLabel>(lp => lp.AddChildContent("Username"))
            .AddChildContent<InputComponent>(ip => ip.Add(c => c.Id, "my-input")));

        var labelFor = cut.Find("label").GetAttribute("for");
        var inputId = cut.Find("input").GetAttribute("id");

        Assert.Equal("my-input", inputId);
        Assert.Equal("my-input", labelFor);
    }

    [Fact]
    public void FieldLabel_ConsumerForOverride_Respected()
    {
        // When a consumer provides an explicit `for` on FieldLabel, it should
        // take precedence over the context-derived ControlId.
        var cut = Render(builder =>
        {
            builder.OpenComponent<FieldRoot>(0);
            builder.AddComponentParameter(1, "ChildContent", (RenderFragment)(b =>
            {
                b.OpenComponent<FieldLabel>(0);
                b.AddComponentParameter(1, "ChildContent", (RenderFragment)(t =>
                    t.AddContent(0, "Username")));
                b.AddMultipleAttributes(2, new Dictionary<string, object> { ["for"] = "custom-control" });
                b.CloseComponent();
            }));
            builder.CloseComponent();
        });

        Assert.Equal("custom-control", cut.Find("label").GetAttribute("for"));
    }

    // Helper component that captures the FieldItemContext cascading value.
    private class ContextCapture : ComponentBase
    {
        [CascadingParameter]
        internal FieldItemContext? Context { get; set; }

        [Parameter]
        public Action<FieldItemContext?>? OnCapture { get; set; }

        protected override void OnParametersSet()
        {
            OnCapture?.Invoke(Context);
        }
    }
